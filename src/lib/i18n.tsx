function subscribe(cb: () => void) {
  if (typeof window === "undefined") return () => {};
  const handler = (e: StorageEvent) => {
    if (e.key === STORAGE_KEY) cb();
  };
  window.addEventListener("storage", handler);
  window.addEventListener("fastlink:lang", cb as EventListener);
  return () => {
    window.removeEventListener("storage", handler);
    window.removeEventListener("fastlink:lang", cb as EventListener);
  };
}

function getSnapshot(): Lang {
  if (typeof window === "undefined") return DEFAULT_LANG;
  try {
    const v = localStorage.getItem(STORAGE_KEY);
    if (v && (v === "en" || v === "zh" || v === "vi" || v === "th" || v === "ja" || v === "ko")) return v;
  } catch {}
  return DEFAULT_LANG;
}

function getServerSnapshot(): Lang {
  return DEFAULT_LANG;
}

function interpolate(str: string, vars?: Record<string, string | number>) {
  if (!vars) return str;
  return str.replace(/\{(\w+)\}/g, (_, k) => (vars[k] !== undefined ? String(vars[k]) : `{${k}}`));
}

type Ctx = {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (k: string, vars?: Record<string, string | number>) => string;
};
const LangCtx = createContext<Ctx>({
  lang: DEFAULT_LANG,
  setLang: () => {},
  t: (k) => k,
});

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [hydrated, setHydrated] = useState(false);
  const stored = useSyncExternalStore(subscribe, getSnapshot, getServerSnapshot);
  useEffect(() => setHydrated(true), []);
  const lang: Lang = hydrated ? stored : DEFAULT_LANG;

  const setLang = (l: Lang) => {
    try {
      localStorage.setItem(STORAGE_KEY, l);
      window.dispatchEvent(new Event("fastlink:lang"));
    } catch {}
  };

  const t = (k: string, vars?: Record<string, string | number>) => {
    const dict = DICTS[lang] || DICTS.en;
    const val = dict[k] ?? DICTS.en[k] ?? k;
    return interpolate(val, vars);
  };

  return <LangCtx.Provider value={{ lang, setLang, t }}>{children}</LangCtx.Provider>;
}

export function useLang() {
  return useContext(LangCtx);
}
